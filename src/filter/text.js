export function upperCase(text) {
  return text.toUpperCase();
}

export function lowerCase(text) {
  return text.toLowerCase();
}
export function exclaim(text) {
  return text + '!!!';
}
//attending 글자 더하기
export function attending(text) {
  return text + 'attending';
}

export function reply(text, name) {
  return name + text + '!!!';
}
//length 구하기 - test
export function length(array) {
  return array.length;
}
